

public class Aula1 {
    public static void main(String[] args) {
    // Declarando variaveis
    String name = "Marta";
    int age = 30;
    double weight = 65.2;

    // Declarando constante
    final String CPF = "123.456.789-00";

    // Exibindo resultados
    // Concatenando com o sinal de "+"
    System.out.println("The user name is: " + name);
    System.out.println("The user's CPF is: " + CPF);
    System.out.println("The user's age is: " + age + " years");
    System.out.println("The user's weight is: " + weight + " kilos");
    }
}